# 自动换行
print('北京')
print('欢迎你！！！')
# 自定义换行  \n
print('北京\n欢迎\n你！！！')
print('-----------------')
print('北京北京\t欢迎你！！！')
print('hello\ttoooo')  # hello是5个字符，一个制表位是8个宁符 8-5=3
print('hellotoooo')

print('老师说:\'好好学习，天天向上\'')
print('老师说:\"好好学习，天天向上\"')

#原字符，使转义字符失效的符号r或R
print(r'北京\n欢迎\n你！！！')
print(R'北京\n欢迎\n你！！！')
